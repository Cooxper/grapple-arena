def draw(screen, cam_x, cam_y):
    sw, sh = screen.get_width(), screen.get_height()
    tx0 = max(0, cam_x // TILE_SIZE)
    ty0 = max(0, cam_y // TILE_SIZE)
    tx1 = min(MAP_W, cam_x // TILE_SIZE + sw // TILE_SIZE + 2)
    ty1 = min(MAP_H, cam_y // TILE_SIZE + sh // TILE_SIZE + 2)
    for ty in range(ty0, ty1):
        for tx in range(tx0, tx1):
            tid = MAP_DATA[ty * MAP_W + tx]
            if tid == TILE_AIR:
                continue
            col  = TILE_COLORS.get(tid, (80, 80, 80))
            ecol = TILE_EDGE.get(tid, (60, 60, 60))
            rx = tx * TILE_SIZE - cam_x
            ry = ty * TILE_SIZE - cam_y
            pygame.draw.rect(screen, col,  (rx, ry, TILE_SIZE, TILE_SIZE))
            pygame.draw.rect(screen, ecol, (rx, ry, TILE_SIZE, TILE_SIZE), 1)
            if tid == TILE_SOLID:
                pygame.draw.line(screen, (100, 130, 180), (rx+2, ry+2), (rx+TILE_SIZE-3, ry+2), 1)